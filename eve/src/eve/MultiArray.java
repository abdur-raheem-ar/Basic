package eve;

public class MultiArray {

	public static void main(String[] args) {
		int[][] arr = new int[3][3];
		for (int i=0;i<1;i++) {
			for (int j=0;j<arr[i].length;j++) {
				System.out.print(j + "\t");
			}
			System.out.println();
			
		}
		for (int i=0;i<1;i++) {
			for (int j=10;j<13;j++) {
				System.out.print(j + "\t");
			}
			System.out.println();
			
		}
		for (int i=0;i<1;i++) {
			for (int j=20;j<23;j++) {
				System.out.print(j + "\t");
			}
			System.out.println();
			
		}
	}

}
